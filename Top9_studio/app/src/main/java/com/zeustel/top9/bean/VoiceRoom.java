public class VoiceRoom implements Serializable {
    private int id;
    //房间序列号
    private String serialNum;
    //房间创建人
    private SubUserInfo subUserInfo;
    //所属游戏id
    private int gameId;
    //创建时间
    private long time;
    //房间名
    private String name;
    //房间内人数
    //    private int count;
    private List<SpeakUser> members;

    public int getGameId() {
        return gameId;
    }

    public void setGameId(int gameId) {
        this.gameId = gameId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public List<SpeakUser> getMembers() {
        return members;
    }

    public void setMembers(List<SpeakUser> members) {
        this.members = members;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSerialNum() {
        return serialNum;
    }

    public void setSerialNum(String serialNum) {
        this.serialNum = serialNum;
    }

    public SubUserInfo getSubUserInfo() {
        return subUserInfo;
    }

    public void setSubUserInfo(SubUserInfo subUserInfo) {
        this.subUserInfo = subUserInfo;
    }

    public long getTime() {
        return time;
    }

    public void setTime(long time) {
        this.time = time;
    }
}
